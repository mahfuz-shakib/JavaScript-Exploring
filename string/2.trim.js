// trim()  --->  remove whitespace from starting and ending position
            //   couldn't remove whitespace from any middle position
const drink='water'
console.log(drink.trim()) // water
const drink2='  juice'
console.log(drink2.trim()) // juice
const drink3='milk  '
console.log(drink3.trim()) // milk
const drink4='mohabbater shorbat' 
console.log(drink4.trim())  // mohabbater shorbat